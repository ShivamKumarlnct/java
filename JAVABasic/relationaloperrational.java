public class relationaloperrational {
    public static void main(String args[]){
        int a=12;
        int b=11;
        int c=3;
        int d=3;
        //equal to
        System.out.println(a==b);//both value is not equal show false
        System.out.println(c==d);//both value is equal show true
        //not equal to
        System.out.println("not equal="+(c!=d));
        System.out.println(a!=b);
        //greater than
        System.out.println("greater="+(a>b));
        System.out.println(b>a);
    }
}
