public class logicaloperator {
    public static void main(String args[]){
        System.out.println((4>2)&&(6>1));//logical AND
        System.out.println((3>1)||(1>2));//logical or
        System.out.println(!(4>2));//logical not

      //  assignment operator
      int a=10;
        // a+=10;//a=a+10=20
        // a-=10;
        a*=10;
        // a/=10;
      System.out.println(a);
    }
    
}
