public class unaryoperator{
    public static void main(String args[]){
        int a=10;
        int b=++a;
        System.out.println(a);//change the value
        System.out.println(b);//than use the value

        int c=20;
        int d=c++;
        System.out.println(d);//value use 
        System.out.println(c);//than change

        int e=10;
        int f=--e;
        int g=e--;
        System.out.println(e);
        System.out.println(f);
        System.out.println(g);


    }
}
